//Edicione strings
fun aMayorQueb (valor1:String, valor2:String): String {
    if (valor1>valor2) {
        return "true"
    }
    else {
        return "false"
    }
}
//Edicion coma flotante
fun aMayorQueb (valor1:Double, valor2:Double): String {
    if (valor1>valor2) {
        return "true"
    }
    else {
        return "false"
    }
}
//Edicion enteros
fun aMayorQueb (valor1:Int, valor2:Int): String {
    if (valor1>valor2) {
        return "true"
    }
    else {
        return "false"
    }
}
